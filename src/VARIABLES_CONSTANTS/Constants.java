public class Constants {
    public static void main(String[] args) {
        /*Constants in Java
         The main difference between variable and constant is: The constant never changes its value

         USE final for declaration constants for example:

         USE CAPITAL LETTERS FOR DECLARATION CONSTANT AND HYPHENS: MY_CONSTANT
         */

        final int MY_CONSTANT = 20;
        System.out.println("My constant is: " + MY_CONSTANT);
    }
}
